    static int helper(int[][] arr, int row, int col){
        if(arr[row][col] == 1){
            return 0;
        }

        arr[0][0] = 1;

        // row
        for (int i = 1; i < arr[0].length; i++) {
            if(arr[0][i] == 1){
                arr[0][i] = 0;
            } else{
                arr[0][i] = arr[0][i-1];
            }
        }
        // col
        for (int i = 1; i < arr.length; i++) {
            if(arr[i][0] == 1){
                arr[i][0] = 0;
            } else{
                arr[i][0] = arr[i-1][0];
            }
        }

        for(int r=1; r < arr.length; r++){
            for(int c = 1; c<arr[0].length; c++){
                if(arr[r][c] == 0){
                    arr[r][c] = arr[r-1][c] + arr[r][c-1];
                } else{
                    arr[r][c] = 0;
                }
            }
        }

        return arr[arr.length-1][arr[0].length-1];
    }
